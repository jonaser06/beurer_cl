if (!function_exists('inputback')) {

    function inputback($post) {
        $pag=$post['pagina'];
        $prohibidas = implode('|', array(
            'dataTable-',
            'pagina',
        ));

        $expresion = '/^(' . $prohibidas . ')+/';
        $output = array();

        foreach ($post as $key => $value) {
            if (!preg_match($expresion, $key)) {
                $output[] = array(
                    'nombre' => $key,
                    'valor' => $value,
                    'idpagina'=>$pag
                );
            }
        }

        return $output;
    }

}

