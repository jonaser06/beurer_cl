if (!function_exists('fechaHumana')) {

    function fechaHumana($fecha = NULL, $tipo = 1, $full = TRUE) {
		
        $fecha = strtotime($fecha);

        $num = date('d', $fecha);
        $dia_adelante = date('w', $fecha);
        $mes = date('n', $fecha);
        $anio = date('Y', $fecha);

        //$dia_adelantes = array('Domingo', 'Lunes', 'Martes', 'Mi&eacute;rcoles', 'Jueves', 'Viernes', 'S&aacute;bado');

        if ($full) {
            $meses = array('', 'Enero', 'Febrero', 'Marzo', 'Abril', 'Mayo', 'Junio', 'Julio', 'Agosto', 'Setiembre', 'Octubre', 'Noviembre', 'Diciembre');
        } else {
            $meses = array('', 'Ene', 'Feb', 'Mar', 'Abr', 'May', 'Jun', 'Jul', 'Agos', 'Set', 'Oct', 'Nov', 'Dic');
        }		
		
		switch ($tipo) {
			case 2:
				$salida = $num . '-' . $meses[$mes] .'-'. $anio;
				break;
			case 3:
				$salida = $num . ' de ' . $meses[$mes] . ' del ' . $anio;
				break;
			default:
				$salida = $meses[$mes] . ' ' . $anio;
				break;
		}
		
		return $salida;
    }

